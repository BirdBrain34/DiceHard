        public static void DisplayRoom(char[,] room,int dicePoints,Player player,Enemy enemy)
        {
            for (int y = 0; y < room.GetLength(0); y++)
            {
                for (int x = 0; x < room.GetLength(1); x++)
                {
                    if (x == PlayerMoveSet.playerX && y == PlayerMoveSet.playerY)
                    {
                        Console.ForegroundColor = ConsoleColor.Blue;
                        Console.Write('@');
                        Console.ResetColor();
                    }
                    else if (room[y, x] == '$')
                    {
                        Console.ForegroundColor = ConsoleColor.Yellow; // Set color to yellow for treasure chests
                        Console.Write('$');
                        Console.ResetColor(); // Reset color after printing '$'
                    }

                    else if (room[y, x] == '#')
                    {
                        Console.ForegroundColor = ConsoleColor.DarkMagenta; // Set color to purple for walls
                        Console.Write('#');
                        Console.ResetColor(); // Reset color after printing '#'
                    }
                    else
                    {
                        bool isEnemy = false;

                        for (int i = 0; i < enemy.EnemyXROOM1.Length; i++)
                        {
                            if (x == enemy.EnemyXROOM1[i] && y == enemy.EnemyYROOM1[i])
                            {
                                isEnemy = true;
                                break;
                            }
                        }

                        if (isEnemy)
                        {
                            Console.ForegroundColor = ConsoleColor.Cyan;
                            Console.Write('E');
                            Console.ResetColor();
                        }
                        else
                        {
                            Console.Write(room[y, x]);
                        }
                    }
                }
                Console.WriteLine();
            }
            Console.WriteLine($"Use ENTER to attack the enemy.");
            Console.WriteLine("Use WASD to move");
        }
    }
